#### Operadores Matemáticos

print("================================= ADIÇÃO ============================================")
### Soma 

# Variávei com os valores já definidos
soma1 = 10
soma2 = 20

# Somando os valores e salva na var resultadoSoma
resultadoSoma = soma1 + soma2

# Exibir o resultado da soma
print(f"A soma de {soma1} mais {soma2}, é {resultadoSoma}")

print("================================= SUBTRAÇÃO ============================================")
### Subtração

# Variávei com os valores já definidos
sub1 = 100
