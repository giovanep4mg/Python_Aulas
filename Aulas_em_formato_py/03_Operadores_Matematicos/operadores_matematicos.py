print("================================= Divisão Inteira (//) ============================================")

### Divisão Inteira (//) =>>  Divide dois números e retorna apenas a parte inteira.tira decimais do número

# Variávei com os valores já definidos
divInt_1 = 10
divInt_2 = 3

# Somando os valores e salva na var resultadoSoma
ResultdivInteira = divInt_1 // divInt_2

# Exibir o resultado da soma
print(f"A Divisão Inteira de {divInt_1} por {divInt_2}, é {ResultdivInteira}")

