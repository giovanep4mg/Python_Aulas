#  Convertendo Tipos de Dados

# Aqui está no formato string => texto
idade = "23"

# Converte para "número inteiro"
idade_convertido = int(idade)

# Aqui o formato é float => números decimais
altura = 1.75

# Converte para string => texto
altura_texto = str(altura)

# Aqui o formato é inteiro => números inteiro
numero = 10

# Converte para float => números decimais
numero_decimal = float(numero)

# Exibirá o tipo 
print("Idade convertida: ", type(idade_convertido))
print("Altura convertida: ", type(altura_texto))
print("Número convertido: ", type(numero_decimal))