######                 Tuplas?
##   Tuplas são coleções imutáveis de elementos.
#### Depois de criadas não tem como mexer como nas listas


# Criando uma Tupla de cores
cores = ("vermelho", "azul", "verde")

# Exibir na tela
print(f"Tuplas: {cores[1]}")  # Tuplas: azul

