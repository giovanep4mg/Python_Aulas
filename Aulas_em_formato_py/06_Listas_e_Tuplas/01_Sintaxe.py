
#       Uma lista no Python é uma coleção ordenada e mutável de elementos.
#       Ela pode conter diferentes tipos de dados (números, strings, outras listas, etc.).


# Sintaxe, como criar uma lista
frutas = ["banana","maça","pera","uva"]

automoveis = ["gol","fusca","celta","uno"]

nomes = ["Giovani","Pedro","João","Marcos"]

# Exibir na tela
print(f"Frutas: {frutas}") # saída => ['banana', 'maça', 'pera', 'uva']

print(f"Carros: {automoveis}") # saída => Carros: ['gol', 'fusca', 'celta', 'uno'] 

print(f"Nomes: {nomes}") # saída => Nomes: ['Giovani', 'Pedro', 'João', 'Marcos']